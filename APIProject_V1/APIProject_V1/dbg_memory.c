#include "dbg_memory.h"

void InitCrtCheck()
{
	// enable debug heap //////////////////////////////////////////
#ifdef _DEBUG
	_CrtSetDbgFlag(_CRTDBG_ALLOC_MEM_DF | _CRTDBG_LEAK_CHECK_DF);
#endif
	///////////////////////////////////////////////////////////////
}
void EndCrtCheck()
{
	// check heap status //////////////////////////////////////////
#ifdef _DEBUG
	_ASSERT(_CrtCheckMemory());
#endif
	///////////////////////////////////////////////////////////////
}